public class SwitchCase {
    public static void main(String[] args) {
        int valor = 0;

        switch (valor) {
            case 1:
                System.out.println("O valor é 1!");
                break;
            case 5:
                System.out.println("O valor é 5!");
                break;
            default:
                System.out.println("O valor não foi identificado!");
        }
    }
}
